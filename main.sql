CREATE TABLE Product (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT UNIQUE,
  category TEXT,
  price REAL,
  discount REAL DEFAULT 0,
  quantity INTEGER,
  measurement TEXT,
  producer TEXT, 
  country TEXT,
  supplier TEXT,
  date_of_delivery DATE CHECK(date_of_delivery >= DATE('now')),
  expire_date DATE CHECK(expire_date >= DATE('now'))
);

